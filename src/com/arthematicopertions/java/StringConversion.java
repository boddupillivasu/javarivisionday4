package com.arthematicopertions.java;

public class StringConversion {

    public static void main(String[] args) {


        char letter = 'A';

        //manual conversion
        int asciiValue = (int) letter;
        System.out.println("the ascii Value of A:" + asciiValue);
 
        int value = 76;
        //manual conversion
        char alphabet = (char) value;
        System.out.println("the alphabet  of 76 is:" + alphabet);


    }
}
